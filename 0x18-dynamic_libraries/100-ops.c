#include "libs.h"

/**
 * add - add two numbers together
 * @a: first num
 * @b: second num
 *
 * Returns: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts a number from another
 * @a: first num
 * @b: second num
 *
 * Returns: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two numbers
 * @a: first num
 * @b: second num
 *
 * Returns: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides a num by another
 * @a: first num
 * @b: second num
 *
 * Returns: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - divides a number by another
 * @a: first num
 * @b: second num
 *
 * Returns: the remainder
 */
int mod(int a, int b)
{
	return (a % b);
}

